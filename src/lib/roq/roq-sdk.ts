import { type CheckSelect, fetcher, marshal, makeUrl, RoqPlatformClient, BrowserCookieAuth } from '@roq/client';
import { Fetcher, SWRConfiguration } from 'swr';
import type { Prisma, park, user } from './types';
import { DefaultArgs, GetFindResult } from './types/runtime/library';

export type RequestOptions = Record<string, any>;
export type SWRRequestOptions<Result, Error = any> = {
  disabled?: boolean;
  initialData?: Result;
} & SWRConfiguration<Result, Error, Fetcher<Result>>;

export const defaultEndpoint = process.env.NEXT_PUBLIC_API_URL || process.env.API_URL;

interface ParkSDK {
  create: <T extends Prisma.parkCreateArgs, R extends CheckSelect<T, park, Prisma.parkGetPayload<T>>>(
    data: Prisma.SelectSubset<T, Prisma.parkCreateArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  createMany: <T extends Prisma.parkCreateManyArgs, R extends Prisma.BatchPayload>(
    args: Prisma.SelectSubset<T, Prisma.parkCreateManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  update: <T extends Prisma.parkUpdateArgs, R extends Prisma.parkGetPayload<T>>(
    data: Prisma.SelectSubset<T, Prisma.parkUpdateArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  updateMany: <T extends Prisma.parkUpdateManyArgs, R extends Prisma.BatchPayload>(
    args: Prisma.SelectSubset<T, Prisma.parkUpdateManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  upsert: <T extends Prisma.parkUpsertArgs, R extends Prisma.parkGetPayload<T>>(
    data: Prisma.SelectSubset<T, Prisma.parkUpsertArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  delete: <T extends Prisma.parkDeleteArgs, R extends Prisma.parkGetPayload<T>>(
    data: Prisma.SelectSubset<T, Prisma.parkDeleteArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  deleteMany: <T extends Prisma.parkDeleteManyArgs, R extends Prisma.BatchPayload>(
    args: Prisma.SelectSubset<T, Prisma.parkDeleteManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  findMany: <T extends Prisma.parkFindManyArgs, R extends GetFindResult<Prisma.$parkPayload<DefaultArgs>, T>[]>(
    args: Prisma.SelectSubset<T, Prisma.parkFindManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  count: <T extends Prisma.parkCountArgs, R extends number>(
    args: Prisma.SelectSubset<T, Prisma.parkCountArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  findManyWithCount: <
    T extends Prisma.parkFindManyArgs,
    R extends GetFindResult<Prisma.$parkPayload<DefaultArgs>, T>[],
    C extends number,
  >(
    args: Prisma.SelectSubset<T, Prisma.parkFindManyArgs>,
    options?: RequestOptions,
  ) => Promise<{ data: R; count: C }>;

  findFirst: <T extends Prisma.parkFindFirstArgs, R extends GetFindResult<Prisma.$parkPayload<DefaultArgs>, T>>(
    args?: Prisma.SelectSubset<T, Prisma.parkFindFirstArgs>,
    options?: RequestOptions,
  ) => Promise<R>;
}

